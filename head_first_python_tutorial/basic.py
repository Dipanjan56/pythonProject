# if else

y = 5

if y > 6:
    x = 10
else:
    x = 20

print(f'if else: x = {x}, y = {y}')

# -----------------------------------------

# if else using ternary operator

x = 10 if y > 6 else 20

print(f'if else using ternary operator: x = {x}, y = {y}')


